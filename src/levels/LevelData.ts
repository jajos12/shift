// ============================================================
//  LevelData.ts — Level definitions as 2D number arrays
// ============================================================
//
//  GAME DEV CONCEPT: Level Design with Tile Grids
//  ------------------------------------------------
//  Levels in 2D games are grids of numbers. Each number = a tile type:
//    0 = empty (air — player falls through)
//    1 = solid ground (player collides with this)
//
//  SHIFT has TWO grids per level — one for Lumina, one for Umbra.
//  When you "shift", the active grid swaps. The key to puzzle design:
//  put GAPS in one dimension where the OTHER dimension has PLATFORMS.
//
//  IMPORTANT PHYSICS CONSTRAINT:
//  Max jump height ≈ 3 tiles (110px). Max horizontal reach ≈ 5 tiles.
//  So all platforms must be within 3 tiles vertically to be reachable!
//
//  The grid is 30 tiles wide × 20 tiles tall (960×640 pixels)
// ============================================================

export interface EnemySpawn {
  type: 'patrol' | 'chase';
  x: number;  // Tile coordinates
  y: number;
  patrolRange?: number;     // For patrol enemies (pixels)
  detectionRange?: number;  // For chase enemies (pixels)
}

export interface LevelDefinition {
  name: string;
  subtitle: string;
  lumina: number[][];
  umbra: number[][];
  playerStart: { x: number; y: number };  // Tile coordinates (not pixels!)
  exit: { x: number; y: number };
  enemies?: EnemySpawn[];
}

